'use strict';
// ─────────────────────────────────────────────────────────────────────────────
// execute.js — apply a docs-action-plan/v1 by creating files on a new branch
// and opening a PR.  Called from actions/github-script inside execute-plan.
// ─────────────────────────────────────────────────────────────────────────────

const core = require('@actions/core');

async function executeDocsPlan({ github, context, env }) {
  const planStr = env.ACTION_PLAN || '';
  const runId   = env.RUN_ID     || '';

  if (!planStr) {
    console.log('No action plan provided; nothing to execute.');
    return;
  }

  let plan;
  try {
    plan = JSON.parse(planStr);
  } catch (e) {
    console.log('Failed to parse action plan JSON:', e.message);
    return;
  }

  if (plan.skip_reason) {
    console.log('Agent returned skip_reason:', plan.skip_reason);
    return;
  }

  const updates = (plan.updates || []).filter(u => u.confidence === 'high');
  if (updates.length === 0) {
    console.log('No high-confidence updates in plan; skipping PR creation.');
    return;
  }

  const { owner, repo } = context.repo;
  const baseSha = context.sha;
  const branch  = `docs/auto-sync-${runId}`;

  // Create the working branch
  await github.rest.git.createRef({
    owner, repo,
    ref: `refs/heads/${branch}`,
    sha: baseSha,
  });
  console.log(`Created branch: ${branch}`);

  for (const update of updates) {
    await applyUpdate({ github, owner, repo, branch, update });
  }

  const pr = await github.rest.pulls.create({
    owner, repo,
    title: buildTitle(plan),
    body:  buildBody(plan, context, runId),
    head:  branch,
    base:  context.payload.repository.default_branch || 'main',
  });

  // Best-effort label application
  try {
    await github.rest.issues.addLabels({
      owner, repo,
      issue_number: pr.data.number,
      labels: ['documentation', 'automated'],
    });
  } catch (_) { /* labels may not exist in the repo */ }

  console.log('Opened PR:', pr.data.html_url);
  core.setOutput('pr-url',    pr.data.html_url);
  core.setOutput('pr-number', String(pr.data.number));
}

async function applyUpdate({ github, owner, repo, branch, update }) {
  // Hard allowlist: only permit doc-safe paths
  const allowed = /^(docs\/|CHANGELOG\.md$|README\.md$)/.test(update.file || '');
  if (!allowed) {
    throw new Error(`Refusing to modify non-doc path: ${update.file}`);
  }

  let content = update.content || '';
  let existingSha;

  if (update.type === 'modify') {
    // Fetch the existing file and replace the named section (not append)
    try {
      const resp = await github.rest.repos.getContent({
        owner, repo,
        path: update.file,
        ref:  branch,
      });
      const existing = Buffer.from(resp.data.content, 'base64').toString('utf8');
      existingSha = resp.data.sha;

      if (update.section && existing.includes(update.section)) {
        const sectionStart = existing.indexOf(update.section);
        const nextSection  = existing.indexOf('\n## ', sectionStart + 1);
        const sectionEnd   = nextSection !== -1 ? nextSection : existing.length;
        content =
          existing.slice(0, sectionStart).replace(/\s*$/, '') +
          '\n\n' +
          (update.content || '').trim() +
          '\n\n' +
          existing.slice(sectionEnd).replace(/^\s*/, '');
      } else {
        content = existing + '\n\n' + update.content;
      }
    } catch (_) {
      // File doesn't exist yet — fall through to create
    }
  }

  await github.rest.repos.createOrUpdateFileContents({
    owner, repo,
    path:    update.file,
    message: `docs: ${update.reason}`,
    content: Buffer.from(content).toString('base64'),
    branch,
    ...(existingSha ? { sha: existingSha } : {}),
  });
  console.log(`Applied update (${update.type}): ${update.file}`);
}

function buildTitle(plan) {
  const summary = (plan.summary || 'automated documentation update').slice(0, 72);
  return `docs: ${summary}`;
}

function buildBody(plan, context, runId) {
  const serverUrl = process.env.GITHUB_SERVER_URL || 'https://github.com';
  const runUrl    = `${serverUrl}/${context.repo.owner}/${context.repo.repo}/actions/runs/${runId}`;

  const changeLines = (plan.updates || [])
    .map(u => `- **${u.type}** \`${u.file}\`: ${u.reason}`)
    .join('\n') || '_none_';

  const skippedLines = (plan.skipped || [])
    .map(s => `- \`${s.file}\`: ${s.reason}`)
    .join('\n') || '_none_';

  return [
    '## 📝 Automated Documentation Update',
    '',
    plan.summary || '',
    '',
    '## Changes',
    changeLines,
    '',
    '## Skipped (low confidence)',
    skippedLines,
    '',
    '---',
    `*Auto-generated by OpenCI Docs Agent · [View run](${runUrl})*`,
  ].join('\n');
}

module.exports = { executeDocsPlan };
